#pragma once

#include <layout/base.hpp>

#include <string>

namespace layout
{

// forward declare
class RouteController;


///////////////////////////////////////////////////////////////////////////////
/// Interface to access a route. A route is a named group of actuators with an associated state that they
/// can all be set to at once
///
///////////////////////////////////////////////////////////////////////////////
class Route : public ComponentDerived<RouteController>
    {
public:
        
    ///////////////////////////////////////////////////////////////////////////////
    /// Constructor
    ///
    /// @param[in]  controller      Controller controlling this route
    /// @param[in]  name                   Route friendly name
    /// @param[in]  id                       Unique ID of route
    ///
    ///////////////////////////////////////////////////////////////////////////////
    Route (RouteController*     controller,
           const std::string&   name,
           size_t               id);

    ///////////////////////////////////////////////////////////////////////////////
    /// Get the friendly name of this route
    ///
    /// @return     Route friendly name
    ///
    ///////////////////////////////////////////////////////////////////////////////
    std::string getName () const { return m_name; }

    ///////////////////////////////////////////////////////////////////////////////
    /// Activate this route
    ///
    ///////////////////////////////////////////////////////////////////////////////
    void set ();

    ///////////////////////////////////////////////////////////////////////////////
    /// Request control of this route
    ///
    ///////////////////////////////////////////////////////////////////////////////
    void request ();

    ///////////////////////////////////////////////////////////////////////////////
    /// Release control of this route
    ///
    ///////////////////////////////////////////////////////////////////////////////
    void release ();

private:
    std::string m_name; ////< Friendly name of route
    };


///////////////////////////////////////////////////////////////////////////////
/// Interface for the route class to communicate with the controller
///
///////////////////////////////////////////////////////////////////////////////
class RouteController : public ControllerBase<Route>
    {
    friend class Route;
public:
    ///////////////////////////////////////////////////////////////////////////////
    /// Activate a route
    ///
    /// @return     Unique ID of route to activate
    ///
    ///////////////////////////////////////////////////////////////////////////////
    virtual void setRoute (size_t id) = 0;

    ///////////////////////////////////////////////////////////////////////////////
    /// Reuest control of a route
    ///
    /// @return     Unique ID of route to request control of
    ///
    ///////////////////////////////////////////////////////////////////////////////
    virtual void requestRouteControl (size_t id) = 0;

    ///////////////////////////////////////////////////////////////////////////////
    /// Release control of a route
    ///
    /// @return     Unique ID of route to release control of
    ///
    ///////////////////////////////////////////////////////////////////////////////
    virtual void releaseRouteControl (size_t id) = 0;
    };



} // namespace layout
