#pragma once

#include <ui/common/formdialog.hpp>

#include <QDialog>
#include <QLineEdit>

namespace ui::common
{

//////////////////////////////////////////////////////////////////////////////
/// Simple dialog with a signle field to enter a string
///
//////////////////////////////////////////////////////////////////////////////
class StringDialog : public FormDialog
    {
public:
    //////////////////////////////////////////////////////////////////////////////
    /// Constructor
    ///
    /// @param[in]  parent      Parent widget
    ///
    //////////////////////////////////////////////////////////////////////////////
    explicit StringDialog (QWidget* parent);

    //////////////////////////////////////////////////////////////////////////////
    /// Get the text that has been entered
    ///
    /// @return     Entered text
    ///
    //////////////////////////////////////////////////////////////////////////////
    QString getText () const { return m_edit->text (); }

    //////////////////////////////////////////////////////////////////////////////
    /// Set the text in the dialog field
    ///
    /// @param[in]  text        Text to set
    ///
    //////////////////////////////////////////////////////////////////////////////
    void setText (const QString& text) { m_edit->setText (text); }

    //////////////////////////////////////////////////////////////////////////////
    /// Set the placeholder text for the text field
    ///
    /// @param[in]  text        Placeholder text
    ///
    //////////////////////////////////////////////////////////////////////////////
    void setPlaceholder (const QString& text) { m_edit->setPlaceholderText (text); }

    //////////////////////////////////////////////////////////////////////////////
    /// Set the text validator
    ///
    /// @param[in]  validator       Text validator
    ///
    //////////////////////////////////////////////////////////////////////////////
    void setValidator (QValidator* validator);

private:
    QLineEdit*  m_edit;             ///< Line edit widget
    QValidator* m_validator = NULL; ///< Text validator

    //////////////////////////////////////////////////////////////////////////////
    /// Check if the text edit has valid input
    ///
    /// @return     True if this has valid input
    ///
    //////////////////////////////////////////////////////////////////////////////
    virtual bool hasAcceptableInput () const override { return m_edit->hasAcceptableInput (); }
    };
}
