#pragma once

#include <utils/device.hpp>

namespace ui::common
{

//////////////////////////////////////////////////////////////////////////////
/// Base class, defines interface for a field that contains host info
///
//////////////////////////////////////////////////////////////////////////////
class AbstractHostField
    {
public:
    //////////////////////////////////////////////////////////////////////////////
    /// Virtual destructor
    ///
    //////////////////////////////////////////////////////////////////////////////
    virtual ~AbstractHostField () {};

    //////////////////////////////////////////////////////////////////////////////
    /// Get the host info for the entered data
    ///
    /// @return     Host info
    ///
    //////////////////////////////////////////////////////////////////////////////
    virtual utils::device::HostInfo getHostInfo () const = 0;

    //////////////////////////////////////////////////////////////////////////////
    /// Check if the info entered is valid
    ///
    /// @return     True if data is valid
    ///
    //////////////////////////////////////////////////////////////////////////////
    virtual bool hasAcceptableInput () const = 0;
    };

} // namespace ui::common